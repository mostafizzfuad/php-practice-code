<?php

// pre increment
$a = 10;
$result = ++$a;  // first increment then assign
echo $result;

echo PHP_EOL;
echo PHP_EOL;

// post increment
$b = 10;
$result = $b++;  // first assign then increment
echo $result;


echo PHP_EOL;
echo PHP_EOL;


// pre decrement
$a = 10;
$result = --$a;   // first decrement then assign
echo $result;


echo PHP_EOL;
echo PHP_EOL;

// post increment
$b = 10;
$result = $b--;  // first assign then decrement
echo $result;